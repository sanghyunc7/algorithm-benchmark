class Solution:
    # @profile
    def distinctNames(self, ideas):
        # OPTIMIZED
        # flip key and value
        # t: offee, ime
        # c: offee
        # d: onuts

        # less keys is better when doing a nested loop
        # at max 26 keys
        # runtime: nested loop: 26 * 26. inner loop: o(n)

        # intuitively, key=name[1:] has less chance of having values
        # while key=name[0] will be much higher
        d = defaultdict(set)
        for name in ideas:
            d[name[0]].add(name[1:])

        ans = 0
        values = [v for v in d.values()]
        for i in range(len(values) - 1):
            for j in range(i + 1, len(values)):
                n1 = values[i]
                n2 = values[j]
                left = n1 - n2
                right = n2 - n1
                combinations = len(left) * len(right)
                ans += 2 * combinations
        return ans

    # @profile
    def distinctNames1(self, ideas):
        # make hashmap of name[1:] to name[0]
        # offee: c, t
        # onuts: d
        # ime: t,   m

        # then do nested loop with each name[1:]
        # find left_side * right_side
        # venn_diagram: [ left_side [middle] right_side ]
        # runtime: nested loop: o(n^2). inner loop: o(26)
        d = defaultdict(set)
        for name in ideas:
            d[name[1:]].add(name[0])

        ans = 0
        values = [v for v in d.values()]
        for i in range(len(values) - 1):
            for j in range(i + 1, len(values)):
                n1 = values[i]
                n2 = values[j]
                left = n1 - n2
                right = n2 - n1
                combinations = len(left) * len(right)
                ans += 2 * combinations
        return ans

    # @profile
    def distinctNames2(self, ideas):
        ideas.sort()
        exists = set(ideas)
        ans = set()
        for i in range(len(ideas) - 1):
            for j in range(i + 1, len(ideas)):
                first = ideas[i]
                second = ideas[j]
                if second[0] + first[1:] in exists:
                    continue
                if first[0] + second[1:] in exists:
                    continue
                ans.add((i, j))
                ans.add((j, i))
        return len(ans)


